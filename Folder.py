PTH_EXTENSIONS = ['.pth']

def matrix_loader(path):
    return torch.load(path)

class MatrixFolder(DatasetFolder):
    def __init__(self, root, transform=None, target_transform=None, loader=matrix_loader):
        super(MatrixFolder, self).__init__(root, loader, PTH_EXTENSIONS,
                                          transform=transform,
                                          target_transform=target_transform)
    def __getitem__(self, index):
        return super().__getitem__(index)[0]

class ImageFeatureFolder(dset.ImageFolder):
    def __init__(self, root, feature_dir, transform=None):
        super(ImageFeatureFolder, self).__init__(root=root, transform=transform)
        self.feature = MatrixFolder(feature_dir)
    
    def __getitem__(self, index):
        img, target = super().__getitem__(index)
        feature = self.feature[index]
        return (img, feature)